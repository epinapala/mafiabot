var shuffle = require('shuffle-array');

function formatUptime(uptime) {
    var unit = 'second';
    if (uptime > 60) {
        uptime = uptime / 60;
        unit = 'minute';
    }
    if (uptime > 60) {
        uptime = uptime / 60;
        unit = 'hour';
    }
    if (uptime != 1) {
        unit = unit + 's';
    }

    uptime = uptime + ' ' + unit;
    return uptime;
}

module.exports = {
	shuffle : shuffle,//use shuffle-array module's shuffle for now.
    formatUptime: formatUptime
};